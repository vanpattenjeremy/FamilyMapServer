package DataContainers;

public class Place {

    String country;
    String city;
    float latitude;
    float longitude;


    public String getCountry() {
        return country;
    }

    public String getCity() {
        return city;
    }

    public float getLatitude() {
        return latitude;
    }

    public float getLongitude() {
        return longitude;
    }
}
